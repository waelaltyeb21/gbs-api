const mongoose = require("mongoose");
const SupervisorSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});
const SupervisorModel = mongoose.model("supervisors", SupervisorSchema);
module.exports = SupervisorModel;